n1 = int (input("Inserisci un numero: "))
n2 = int (input("Inserisci un altro numero: "))

somma_dei_quadrati = n1**n1 + n2**n2
print(f"La somma dei quadrati vale {somma_dei_quadrati}")

quadrato_della_somma = (n1 + n2) ** 2
print(f"Il quadrato della somma vale {quadrato_della_somma}")

differenza_dei_quadrati = (n1**n1) - (n2**n2)
print(f"La differenza dei quadrati vale {differenza_dei_quadrati}")

differenza_al_quadrato = (n1 - n2)**2
print(f"La differenza tra i due numeri al quadrato vale {differenza_al_quadrato}")


